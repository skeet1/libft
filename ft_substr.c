#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*sub;

	i = 0;
	if (!s)
		return ((char *)s);
	sub = malloc(len + 1);
	if (!sub || start >= (unsigned int)ft_strlen((char *)s))
		return (sub);
	while (i < len)
	{
		sub[i] = s[start];
		i++;
		start++;
	}
	sub[i] = '\0';
	return (sub);
}
