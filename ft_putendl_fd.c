#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}

int		main(void)
{
	int		fd;

	fd = open("hello", O_CREAT | O_RDWR);

	ft_putendl_fd("i'm here again", fd);
}
